package edu.eci.pdsw.webappsintro.controller;

/**
 *
 * @author Hugo Alvarez
 */
public class Total {
    private int precio,cantidad,total,id;
    private String nombre;

    public Total(int precio, int cantidad, int total, int id, String nombre) {
        this.precio = precio;
        this.cantidad = cantidad;
        this.total = total;
        this.id = id;
        this.nombre = nombre;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
    
    public Total() {
    }

    public int getPrecio() {
        return precio;
    }

    public void setPrecio(int precio) {
        this.precio = precio;
    }

    public int getCantidad() {
        return cantidad;
    }

    public void setCantidad(int cantidad) {
        this.cantidad = cantidad;
    }

    public int getTotal() {
        return precio*cantidad;
    }
    
}
